#include <iostream>
using namespace std;

int main () {
    int num = 1;
    while (1)
    {
        switch(num) {
            case 1: cout << "Number 1" << endl;
            break;
            case 2: cout << "Number 2" << endl;
            break;
            default : cout << "Default Case" << endl;
            
        }
        exit(0);
    }
      
}